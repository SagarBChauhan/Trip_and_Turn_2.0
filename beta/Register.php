        function subform(){
            var data =$("#FormReg_1").serialize();
            $.ajax({
                type: 'POST',
                url: "sendEmail.php",
                data: data,
                beforeSend: function () {
                    $("#info").fadeOut();
                    $("#btn_send").html("Hang on");
                },
                success: function (resp) {
                    if(resp=="ok"){
                        $("#btn_send").html(" Loading &nbsp;<i class='fas fa-spinner fa-spin'></i>");
